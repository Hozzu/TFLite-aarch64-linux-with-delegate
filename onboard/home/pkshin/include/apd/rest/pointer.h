#ifndef LIB_APDREST_INCLUDE_APD_REST_POINTER_H_
#define LIB_APDREST_INCLUDE_APD_REST_POINTER_H_

#include <memory>

namespace apd
{
namespace rest
{

/** \brief The equivalent if std::unique_ptr for apd::rest internal uses
 *
 * For all internal purposes this type shall be favored over std::unique_ptr for compatible
 * future replacement. A custom unique_ptr may be required to meet the requirements of a safe
 * memory concept.
 *
 * For now, Pointer is an alias for std::unique_ptr.
 * \uptrace{SWS_REST_02355}
 */
template <typename T>
using Pointer = std::unique_ptr<T>;

}  // namespace rest
}  // namespace apd

#endif  // LIB_APDREST_INCLUDE_APD_REST_POINTER_H_
